CREATE PACKAGE EMPLOYEES_MANAGEMENT
AS
    FUNCTION HIRE_EMPLOYEES
    (
        LAST_NAME       VARCHAR2,
        JOB_ID          VARCHAR2,
        MANAGER_ID      NUMBER,
        SALARY          NUMBER,
        COMMISSION_PAC  NUMBER,
        DEPARTMENT_ID   NUMBER
    )
    RETURN NUMBER;
    PROCEDURE FIRE_EMPLOYEES
    (
        EMPLOYEE_ID     NUMBER
    );
    PROCEDURE SALARY_RAISE
    (
        EMPLOYEE_ID     NUMBER,
        SALARY_INCR     NUMBER
    );
    NO_SAL EXCEPTION;
END EMPLOYEES_MANAGEMENT;
